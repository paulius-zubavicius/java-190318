package com.vcs.lects.l03.java_types;

public class Knyga {

	private String autorius;
	private String pavadinimas;
	private int metai;
	private boolean vienasAutorius;
	private Boolean paimta;

	public String getAutorius() {
		return autorius;
	}

	public void setAutorius(String autorius) {
		this.autorius = autorius;
	}

	public String getPavadinimas() {
		return pavadinimas;
	}

	public void setPavadinimas(String pavadinimas) {
		this.pavadinimas = pavadinimas;
	}

	public int getMetai() {
		return metai;
	}

	public void setMetai(int metai) {
		this.metai = metai;
	}

	public boolean isVienasAutorius() {
		return vienasAutorius;
	}

	public void setVienasAutorius(boolean vienasAutorius) {
		this.vienasAutorius = vienasAutorius;
	}

	public Boolean getPaimta() {
		return paimta;
	}

	public void setPaimta(Boolean paimta) {
		this.paimta = paimta;
	}
	
	
	
	

}
